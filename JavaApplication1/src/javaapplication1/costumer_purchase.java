    public void setProductDetails(int id, String name, String price, String imgPath) {
        this.currentProductId = id; // Now 'id' is recognized because it's in the ( ) above
        this.productPrice = price; 

        jTextField2.setText(name); 
        jTextField2.setEditable(false); 
        jTextField7.setText(price); 
        jTextField7.setEditable(false);

        if (imgPath != null && !imgPath.isEmpty()) {
            try {
                javax.swing.ImageIcon icon = new javax.swing.ImageIcon(imgPath);
                int width = jPanel1.getWidth() > 0 ? jPanel1.getWidth() : 227;
                int height = jPanel1.getHeight() > 0 ? jPanel1.getHeight() : 204;
                java.awt.Image img = icon.getImage().getScaledInstance(width, height, java.awt.Image.SCALE_SMOOTH);
                javax.swing.JLabel imgLabel = new javax.swing.JLabel(new javax.swing.ImageIcon(img));
                jPanel1.setLayout(new java.awt.BorderLayout());
                jPanel1.removeAll();
                jPanel1.add(imgLabel);
                jPanel1.revalidate();
                jPanel1.repaint();
            } catch (Exception e) {
                System.out.println("Image Error: " + e.getMessage());
            }
        }
    } // Closes setProductDetails

    
